#pragma once

/*
 * config.h — Arduino #2 (모터 컨트롤러) 설정
 *
 * ┌──────────────────────────────────────────────────────┐
 * │ 이 보드의 역할                                        │
 * │  CAN 노드에서 I2C로 받은 명령 → 모터 4개 구동 +       │
 * │  엔코더 카운팅 → 속도/에러 I2C로 돌려줌               │
 * │                                                       │
 * │ 하드웨어                                              │
 * │  아두이노 우노 + 아두이노 모터 쉴드 R3 × 2장 스택     │
 * │  - 쉴드 A 채널 → 왼쪽 쌍 모터 2개                    │
 * │  - 쉴드 B 채널 → 오른쪽 쌍 모터 2개                  │
 * │  * 왼쪽 모터는 배선 극성 뒤집기 (차체 전진 일치)      │
 * │                                                       │
 * │ 동작 모드                                             │
 * │  직진 / 정지 / 후진 (조향 없음)                       │
 * │  단일 채널 2개 = 좌/우 독립 제어, 각 채널은 2모터 공유│
 * └──────────────────────────────────────────────────────┘
 */

/* =========================================================
   I2C 슬레이브 주소
   ========================================================= */
#define MY_I2C_ADDR     0x10    // CAN 노드와 동일한 값 사용

/* =========================================================
   모터 쉴드 핀 배치 (아두이노 모터 쉴드 R3)
   =========================================================
   R3 기본 핀:
     채널 A: DIR=D12, PWM=D3, BRK=D9, SNS=A0
     채널 B: DIR=D13, PWM=D11, BRK=D8, SNS=A1
   ※ 두 쉴드 스택 시 SNS(A0/A1)는 부정확해서 사용 안 함
   ========================================================= */

/* 채널 A — 왼쪽 쌍 (앞왼쪽 + 뒤왼쪽) */
#define PIN_DIR_A       12      // 방향 제어 (HIGH=정방향, LOW=역방향)
#define PIN_PWM_A       3       // PWM 속도 제어 (0~255)
#define PIN_BRK_A       9       // Brake (HIGH=제동, LOW=주행)

/* 채널 B — 오른쪽 쌍 (앞오른쪽 + 뒤오른쪽) */
#define PIN_DIR_B       13
#define PIN_PWM_B       11
#define PIN_BRK_B       8

/* =========================================================
   엔코더 핀 (단일 엔코더)
   =========================================================
   D2: INT0 — 하드웨어 인터럽트 (CHANGE 트리거)
   D4: 방향 판단용 B 채널
   ========================================================= */
#define PIN_ENC_A       2       // 엔코더 A 채널 (INT0)
#define PIN_ENC_B       4       // 엔코더 B 채널
#define ENC_INT_NUM     0       // attachInterrupt(0, ...) = 핀 2

/* =========================================================
   엔코더 실측 상수 (측정 후 수정)
   ========================================================= */
/*
 * PPR: 모터 1회전당 엔코더 펄스 수 (CHANGE 트리거 기준)
 * 측정법: encoder.cpp의 DEBUG_ENCODER=1로 바꾸고 모터를 1바퀴 돌려 count 값 확인
 */
#define ENCODER_PPR     20      // ← 실측 후 수정

/*
 * 바퀴 둘레 (mm) = π × 직경
 * 예) 직경 65mm → 3.14159 × 65 ≈ 204
 */
#define WHEEL_CIRC_MM   204     // ← 실측 후 수정

/* =========================================================
   타이밍 (단위: ms)
   ========================================================= */
#define PERIOD_CTRL_MS        10    // 모터 출력 갱신 주기
#define PERIOD_ENC_CALC_MS    10    // 엔코더 속도 계산 주기
#define TIMEOUT_CMD_MS        200   // I2C 명령 타임아웃
                                    // (CAN 게이트웨이가 10ms 주기로 보내는데
                                    //  200ms 이상 없으면 통신 끊김으로 간주)

/* =========================================================
   에러 플래그
   ========================================================= */
#define ERR_TIMEOUT     0x02    // I2C 명령 타임아웃
